<?php
// Generate a UUID
function generateUUID()
{
    // Format: xxxxxxxx-xxxx-Mxxx-Nxxx-xxxxxxxxxxxx
    return sprintf(
        '%04x%04x-%04x-%04x-%04x-%04x%04x%04x',
        mt_rand(0, 0xffff),
        mt_rand(0, 0xffff),
        mt_rand(0, 0xffff),
        mt_rand(0, 0x0fff) | 0x4000,
        mt_rand(0, 0x3fff) | 0x8000,
        mt_rand(0, 0xffff),
        mt_rand(0, 0xffff),
        mt_rand(0, 0xffff)
    );
}

// Usage
$uuid = generateUUID();
echo "Generated UUID: $uuid";
?>
